    private void tblSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSanPhamMouseClicked
        int row = tblSanPham.getSelectedRow();
        fill(row, listSP);
    }//GEN-LAST:event_tblSanPhamMouseClicked

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        SanPham sanPham = newSanPham();
        JOptionPane.showMessageDialog(this, sanPhamServiceImpl.add(sanPham));
        listSP = sanPhamServiceImpl.getAll();
        showData(listSP);
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int row = tblSanPham.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "???");
        } else {
            String idSP = listSP.get(row).getIdSP();
            SanPham sanPham = newSanPham();
            JOptionPane.showMessageDialog(this, sanPhamServiceImpl.update(sanPham, idSP));
            listSP = sanPhamServiceImpl.getAll();
            showData(listSP);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int row = tblSanPham.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "???");
        } else {
            String idSP = listSP.get(row).getIdSP();
            JOptionPane.showMessageDialog(this, sanPhamServiceImpl.delete(idSP));
            listSP = sanPhamServiceImpl.getAll();
            showData(listSP);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

